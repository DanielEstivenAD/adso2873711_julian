ALTER TABLE JEFE ADD FOREIGN KEY(FK_NUMERO_AREA) REFERENCES AREA(NUMERO_AREA) ;
ALTER TABLE JEFE ADD FOREIGN KEY(FK_CEDULA_EMPLEADO) REFERENCES EMPLEADO(CEDULA_EMPLEADO) ;

